name = "Biswajit"

print(name[0])  # single slicing
print(name[0:8])  # mutliple slicing
print(name[:8])  # mutliple slicing in other form and is same as name[0:8]
print(name[0:])  # mutliple slicing again in other form and is same as name[0:8]
print(name[-8:-1])  # mutliple slicing again in other form and is same as name[1:8]